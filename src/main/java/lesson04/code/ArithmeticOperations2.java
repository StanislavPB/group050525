public class ArithmeticOperations2 {

    public static void main(String[] args) {

        int x = 10;
        int y = 3;

        System.out.println(x / y);
        System.out.println(x % y);


        double result = (double) x / y;

        System.out.println(result);

        System.out.println( 10.0 / 3);
    }
}
